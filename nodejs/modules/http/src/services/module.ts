import { apiUrl } from '../api-url';
import { BuildModuleModel } from '../models/build-module';
import { ModuleModel } from '../models/module';
import { BaseService, ServiceEventEmitter } from './base';

export class ModuleService {
  public emitter = new ServiceEventEmitter<ModuleModel>();
  private baseService = new BaseService<ModuleModel>(this.emitter, ModuleModel);

  /**
   * Returns the number of Records satisfying the query.
   */
  public async count(query: any) {
    const url = this.getUrl();
    return this.baseService.count(query, url);
  }

  /**
   * Creates a Record.
   */
  public async create(json: Partial<ModuleModel & BuildModuleModel>) {
    const url = this.getUrl();
    return this.baseService.create(json, url);
  }

  /**
   * Deletes a Record.
   */
  public async delete(_id: string) {
    const url = this.getUrl();
    return this.baseService.delete(_id, url);
  }

  /**
   * Returns an array of Records satisfying the query.
   */
  public async find(query: any) {
    const url = this.getUrl();
    return this.baseService.find(query, url);
  }

  /**
   * Returns a Record by ID.
   */
  public async findOne(_id: string) {
    const url = this.getUrl();
    return this.baseService.findOne(_id, url);
  }

  /**
   * Updates a Record.
   */
  public async update(_id: string, json: Partial<ModuleModel & BuildModuleModel>) {
    const url = this.getUrl();
    return this.baseService.update(_id, json, url);
  }

  /**
   * Returns the base URL for this Model.
   */
  private getUrl() {
    return `${apiUrl}/modules`;
  }
}

export const moduleService = new ModuleService();
